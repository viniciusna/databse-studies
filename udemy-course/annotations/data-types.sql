-- Principais tipos numéricos
-- INT: um número inteiro
-- numeric(p, s): um número float, onde 'p' é a quantidade de dígitos totais dos números e 's' a quntidade de dígitos depois da vírgula
-- SERIAL: um INT que se auto incrementa, usado para ID's

-- Principais tipos de strings
-- CHAR(n): string de comprimento fixado, 'n' é o tamanho da string
-- VARCHAR(n): string com tamanho máximo de 'n' caracteres
-- TEXT: string sem limite de tamanho

-- Principais tipos de datas
-- TIME: hh:mm:ss
-- DATE: yyyy-mm-dd
-- timestamp: yyyy-mm-dd hh:mm:ss

-- Outros tipos de dados
-- BOOLEAN: True ou False
-- ENUM: define uma lista de opções para o valor da coluna